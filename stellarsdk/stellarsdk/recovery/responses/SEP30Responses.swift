}

/// Response for transaction signing operations.
///
/// Contains the signature generated by the recovery service and the network passphrase
/// used during signing.
public struct Sep30SignatureResponse: Decodable {

    /// Base64 encoded signature produced by signing the transaction with the recovery service's signing key.
    /// This signature should be added to the transaction's signatures.
    public var signature: String

    /// Network passphrase used when generating the signature.
    /// Verifies that the signature is valid for the intended Stellar network.
    public var networkPassphrase: String

    /// Properties to encode and decode
    private enum CodingKeys: String, CodingKey {
        case signature
        case networkPassphrase = "network_passphrase"
    }

    /**
     Initializer - creates a new instance by decoding from the given decoder.

     - Parameter decoder: The decoder containing the data
     */
    public init(from decoder: Decoder) throws {
        let values = try decoder.container(keyedBy: CodingKeys.self)
        signature = try values.decode(String.self, forKey: .signature)
        networkPassphrase = try values.decode(String.self, forKey: .networkPassphrase)
    }
}

